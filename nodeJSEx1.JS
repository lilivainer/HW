const num = 123;

/*
שלב 1: בדיקות התחלקות
בשלב זה אנחנו בודקים האם המספר הנתון (num) מתחלק ב-2, ב-3 או ב-5. כל אחת מהבדיקות מבוצעת באמצעות אופרטור ה-% (מודולו), שבודק את שארית החילוק:

num % 2 === 0: האם שארית החלוקה ב-2 היא 0? אם כן, המספר מתחלק ב-2.
num % 3 === 0: האם שארית החלוקה ב-3 היא 0? אם כן, המספר מתחלק ב-3.
num % 5 === 0: האם שארית החלוקה ב-5 היא 0? אם כן, המספר מתחלק ב-5.
תוצאה של כל אחת מהבדיקות היא בוליאנית:

true אם המספר מתחלק באותו מחלק.
false אם הוא לא מתחלק.
לדוגמה:

אם num = 30, התוצאות יהיו:
divisibleBy2 = true
divisibleBy3 = true
divisibleBy5 = true
*/
// שלב 1: בדיקות התחלקות
const divisibleBy2 = num % 2 === 0;
const divisibleBy3 = num % 3 === 0;
const divisibleBy5 = num % 5 === 0;
/*שלב 2: חישוב כמות המספרים שהמספר מתחלק בהם
כאן אנחנו משתמשים בפעולה Number() כדי להמיר את התוצאות הבוליאניות (true/false) למספרים:

Number(true) מחזיר 1.
Number(false) מחזיר 0.
*/
// שלב 2: חישוב כמות המספרים שהמספר מתחלק בהם
const count =
  Number(divisibleBy2) + Number(divisibleBy3) + Number(divisibleBy5);

/*שלב 3: הדפסת התוצאה
לבסוף, התוצאה של המשתנה count מודפסת למסך. היא מציינת כמה מתוך המחלקים (2, 3, 5) המספר נתון מתחלק בהם.
*/
// שלב 3: הדפסת התוצאה
console.log(count);
