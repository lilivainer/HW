// הגדרת מערך של מספרים שלמים
const numbers = [0, 5, 3, 0, 7, 0, 2, 8, 0, -1, 0];
/*שימוש ב-reduce:

reduce היא פונקציה שמאפשרת לצבור ערכים על בסיס לולאה על המערך.
היא מקבלת פונקציה עם שני פרמטרים:
count (הערך שנצבר עד כה),
num (המספר הנוכחי במערך).
הפונקציה מתחילה בערך התחלתי של 0 (שמיוצג על ידי הארגומנט האחרון של reduce).
הביטוי המותנה:

בתוך ה-reduce, הביטוי num === 0 ? 1 : 0 מחזיר 1 אם המספר הנוכחי שווה ל-0, אחרת מחזיר 0.
התוצאה מתווספת לערך המצטבר count.
*/
// חישוב כמות האפסים בעזרת ביטוי מותנה
const zeroCount = numbers.reduce(
  (count, num) => count + (num === 0 ? 1 : 0),
  0
);
//בסיום הפעלת ה-reduce, מתקבל המספר הכולל של האפסים במערך.
// הדפסת התוצאה
console.log(`כמות האפסים במערך: ${zeroCount}`);
